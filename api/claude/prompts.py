def get_system_prompt() -> str:
    return _PREAMBLE + "\n\n" + _PROMPT_PATH.read_text(encoding="utf-8")


# Cargado una sola vez al importar el módulo → prompt caching efectivo
SYSTEM_PROMPT: str = get_system_prompt()
